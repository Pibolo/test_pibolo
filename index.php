                        <h2>Pourquoi ?</h2><br>
                        
                       
                        <p class="lead">Paris n’est pas l’idéale pour un artisan en matière de circulation, de stationnement, et de ponctualité. Le choix d’une fourgonnette est justifié par sa dimension « utilitaire » mais il implique beaucoup de frais, de charges et ses avantages sont vite rattrapés par ses inconvénients.<br><br>
                        Sensible à mon environnement, aux contraintes infligées par un véhicule à moteur, je fais le choix pour ma société et mon entourage de faire mes interventions à vélo qui me permet d’évoluer de façon très fluide dans la circulation et ainsi d’offrir à ma clientèle un service de qualité, de proximité pour un coût moindre.</p>
                        
