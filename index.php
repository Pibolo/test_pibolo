                                <h3><strong>La loi artisanat, commerce et très petites entreprises.</strong></h3>
                                <p class="lead">
                                Avec un chiffre d’affaires de 2 000 milliards d'euros, soit 25% du PIB et 7 millions de salariés, l'artisanat, le commerce et les très petites entreprises sont indispensables à la vitalité économique et sociale des territoires et fortement créateurs d'emploi. Mais parce que la crise économique touche ces entreprises, elles ont besoin de mesures pour les aider au quotidien.<br><br>

                                Ce texte, largement adopté en première lecture à l'Assemblée nationale (323 voix pour et 1 voix contre), apporte la démonstration de la forte mobilisation de l'ensemble des professionnels concernés et des élus des territoires pour permettre de soutenir la diversité, le développement et la modernisation des commerces de proximité.<br><br>


                                L'objectif est de promouvoir la qualité et les savoir-faire de des artisans. </p><br>

                                <h3><strong>La loi entend : Clarifier la qualité d'artisan</strong></h3><br>

                                <p class="lead">Pour rendre plus lisible la notion d'artisan comme gage de qualité et de savoir-faire auprès des consommateurs, la loi réserve la qualité d'artisan aux seuls détenteurs d'une qualification professionnelle pour le métier qu'ils exercent. La loi supprime la notion "d'artisan qualifié".<br><br>

